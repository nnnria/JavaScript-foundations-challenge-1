/* 
   Create a constant variable of each of the following primitive types:
   - String
   - Number
   - Boolean
   - Null
   - Undefined

   Log out each variable to the terminal at the end.
*/
// Constant String
const myString = "Hello, World";

// Constant Number
const myNumber = 42;

// Constant Boolean
const myBoolean = true;

// Constant Null
const myNull = null;

// Constant Undefined
const myUndefined = undefined;

// Log the variables to the terminal
console.log("Constant String:", myString);
console.log("Constant Number:", myNumber);
console.log("Constant Boolean:", myBoolean);
console.log("Constant Null:", myNull);
console.log("Constant Undefined:", myUndefined);
